function Contents() {
  const navigate = useNavigate();
  const fileInputRef = useRef<HTMLInputElement>(null);
  const { addFiles } = useFiles();

  const handleClick = () => {
    fileInputRef.current?.click();
  };

  const handleFileChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    if (e.target.files && e.target.files.length > 0) {
      const filesArray = Array.from(e.target.files);
      addFiles(filesArray);
      navigate('/preview');
    }
  };

  return (
    <>
      <input
        ref={fileInputRef}
        type="file"
        accept=".pdf,.jpg,.jpeg,.heic,.heif,.png,image/*"
        multiple
        onChange={handleFileChange}
        className="hidden"
      />
      <div
        onClick={handleClick}
        className="content-stretch flex flex-[1_0_0] flex-col gap-[24px] items-center justify-center min-h-px relative rounded-[10px] w-full cursor-pointer"
        data-name="contents"
      >
        <div aria-hidden="true" className="absolute border-3 border-[#423bff] border-dashed inset-0 pointer-events-none rounded-[10px] shadow-[2px_2px_10px_0px_rgba(58,255,193,0.25),-2px_-2px_10px_0px_rgba(133,89,255,0.25)]" />
        <div className="overflow-clip relative shrink-0 size-[148px]" data-name="glass">
          <Component2 />
          <HiconGlassInformationCircle />
        </div>
        <Frame3 />
      </div>
    </>
  );
}

